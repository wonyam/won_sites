				<h4 class="header-3">Causes & Growing Conditions</h4>
		            <h5>
	          			Molds are found everywhere inside and outside, and can grow on almost any substance when moisture is present. Molds reproduce by spores, 
	          			which can be carried by air currents. When these spores land on a moist surface that is suitable for life, they begin to grow.
						<br /><br />
						Common building materials are capable of sustaining mold growth. Flooding, leaky roofs, building maintenance problems, or indoor plumbing 
						problems can lead to mold growth inside. Moisture, vapor commonly condenses on surfaces cooler than the moisture containing air which 
						enables mold to flourish. 
						<br /><br />
						Common building materials, such as plywood, drywall, furring strips, carpets, and carpet padding are food for molds. In carpet, invisible 
						dust and cellulose are the food sources (see also dust mites). After a single incident of water damage occurs in a building, molds grow 
						inside walls and then become dormant until a subsequent incident of high humidity; this illustrates how mold can appear to be a sudden 
						problem, after a previous flood or water incident that did not produce such a problem. The right conditions reactivate mold.
					</h5>
				<div class="h_30"></div>
				<h4 class="header-3">Spores need three things to grow into mold: </h4>
		            <h5>
	          			<ul class="list_circle"> 
							<li>Nutrients: Cellulose is a common food for spores in an indoor environment. It is the part of the cell wall of green plants.</li>
							<li>Moisture: Moisture is required to begin the decaying process caused by the mold.</li>
							<li>Time: Mold growth begins between 24 hours and 10 days from the provision of the growing conditions. There is no known way to date mold.</li>
						</ul>
						The main problem with the presence of mold in buildings is the inhalation of mycotoxins. Molds may produce an identifiable smell.
					</h5>
