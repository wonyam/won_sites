				<h4 class="header-3">Removing one of the three requirements for mold reduces or eliminates the new growth of mold. These three requirements are: </h4>
		            <h5>
		            	1.	Moisture<br />
						2.	Food source for the mold spores (dust, dander, etc.)<br />
						3.	Warmth (mold generally does not grow in cold environments).
						<br /><br />
						HVAC systems can create all three requirements for significant mold growth. The A/C system creates a difference in temperature that 
						allows/causes condensation to occur. The high rate of dusty air movement through an HVAC system may create ample sources of food for the mold.
