			            for plastics.<br /><br />
						Crocidolite was generally mined in Western Australia, Bolivia, and South Africa. The percentage of miners who developed asbestos cancer 
						due to crocidolite exposure stands at a staggering 18 percent. The town of Wittenoom, Australia (population 20,000), where blue asbestos 
						was mined for many years, had more than 1,000 people die of mesothelioma with officials estimating that another 1,000 will eventually perish 
						from asbestos-related diseases. The town is now a ghost town, with only eight residents remaining, and has literally been erased from the map.

