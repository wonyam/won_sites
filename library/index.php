			<h1>MOLD</h1>
            
            <h5>
	            Mold spores are a common component of household and workplace dust.  When spores are present in large quantities, they are a health hazard to 
	            humans, potentially causing allergic reactions and respiratory problems.
				<br /><br />
				Some molds produce mycotoxins that can pose serious health risks to humans and animals. The term "toxic mold" refers to molds that produce 
				mycotoxins, such as Stachybotrys chartarum, not to all molds. Exposure to high levels of mycotoxins can lead to neurological problems and 
				in some cases death. Prolonged exposure can be particularly harmful.
	        </h5>
	          
	          
